    void dangNhap() {

        String manv = txtTenDangNhap.getText();
        String matKhau = new String(txtMatKhau.getPassword());
        try {
            NhanVien nhanVien = dao.findById(manv);
            if (nhanVien != null) {
                String matKhau2 = nhanVien.getMatKhau();
                if (matKhau.equals(matKhau2)) {
                    NhanVien nv = nhanVien;
                    //  System.out.println(nv.getHoTen());
                    Auth.user = nhanVien;
                    DialogHerlper.alert(this, "Đăng nhập thành công!");
                    this.dispose();
                } else {
                    DialogHerlper.alert(this, "Sai mật khẩu!");
                }
            } else {
                DialogHerlper.alert(this, "Sai tên đăng nhập!");
            }

        } catch (Exception e) {
            DialogHerlper.alert(this, "Lỗi truy vấn dữ liệu!");
        }

    }

